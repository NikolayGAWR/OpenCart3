    public function upload() {
        $this->load->language('tool/upload');

        $json = array();


        if (!empty($this->request->files['file']['name'])) {
            $countfiles = count($_FILES['file']['name']);
            for($i=0;$i<$countfiles;$i++){
                // Sanitize the filename
                $filename = $this->request->files['file']['name'][$i];

                // Validate the filename length
                if ((utf8_strlen($filename) < 3) || (utf8_strlen($filename) > 64)) {
                    $json['error'] = $this->language->get('error_filename');
                }

                // Allowed file extension types
                $allowed = array();

                $extension_allowed = preg_replace('~\r?\n~', "\n", $this->config->get('config_file_ext_allowed'));

                $filetypes = explode("\n", $extension_allowed);

                foreach ($filetypes as $filetype) {
                    $allowed[] = trim($filetype);
                }

                if (!in_array(strtolower(substr(strrchr($filename, '.'), 1)), $allowed)) {
                    $json['error'] = $this->language->get('error_filetype');
                }

                // Allowed file mime types
                $allowed = array();

                $mime_allowed = preg_replace('~\r?\n~', "\n", $this->config->get('config_file_mime_allowed'));

                $filetypes = explode("\n", $mime_allowed);

                foreach ($filetypes as $filetype) {
                    $allowed[] = trim($filetype);
                }

                if (!in_array($this->request->files['file']['type'][$i], $allowed)) {
                    $json['error'] = $this->language->get('error_filetype');
                }

                // Check to see if any PHP files are trying to be uploaded
                $content = file_get_contents($this->request->files['file']['tmp_name'][$i]);

                    $json['file'][$i] = $file = $filename;

                    move_uploaded_file($this->request->files['file']['tmp_name'][$i], DIR_UPLOAD . $file);

                    // Скрыть имя загружаемого файла
                    $this->load->model('tool/upload');

                    $json['code'] = $this->model_tool_upload->addUpload($filename, $file);

                    $json['success'] = $this->language->get('text_upload');

                // if (preg_match('/\<\?php/i', $content))
                if (preg_match('/\?php/i', $content)) {
                    $json['error'] = $this->language->get('error_filetype');
                }

                // Вернуть ошибку загрузки
                if ($this->request->files['file']['error'][$i] != UPLOAD_ERR_OK) {
                    $json['error'] = $this->language->get('error_upload_' . $this->request->files['file']['error'][$i]);
                }

            }

        } else {
            $json['error'] = $this->language->get('error_upload');
        }

        $this->response->addHeader('Content-Type: application/json');
        $this->response->setOutput(json_encode($json));
    }

    //---------------------------------------------------------------------------------------------------


	protected function validate() {
		if ((utf8_strlen($this->request->post['name']) < 3) || (utf8_strlen($this->request->post['name']) > 32)) {
			$this->error['name'] = $this->language->get('error_name');
		}

		if (!filter_var($this->request->post['email'], FILTER_VALIDATE_EMAIL)) {
			$this->error['email'] = $this->language->get('error_email');
		}

		if ((utf8_strlen($this->request->post['enquiry']) < 10) || (utf8_strlen($this->request->post['enquiry']) > 3000)) {
			$this->error['enquiry'] = $this->language->get('error_enquiry');
		}

        if ((empty($this->request->post['file']))) {
            $this->error['file'] = $this->language->get('error_file');
        }

		// Captcha
		if ($this->config->get('captcha_' . $this->config->get('config_captcha') . '_status') && in_array('contact', (array)$this->config->get('config_captcha_page'))) {
			$captcha = $this->load->controller('extension/captcha/' . $this->config->get('config_captcha') . '/validate');

			if ($captcha) {
				$this->error['captcha'] = $captcha;
			}
		}

		return !$this->error;
	}

